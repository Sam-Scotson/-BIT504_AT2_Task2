package TicTacToe;

/**
 * This enum is used to track the content of each cell on the game board for the players' moves.
 * Each cell in the game can be in one of three states: Empty, Cross, or Nought.
 */
public enum Player {
    Empty,  // Represents an empty cell
    Cross,  // Represents a cross cell ('X') 
    Nought  // Represents a nought cell ('O') 
}